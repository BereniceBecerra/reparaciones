    private void jbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarActionPerformed
        if (tId.getText() != null){
            int id=Integer.parseInt(tId.getText());
            String nombre=tNombre.getText();
            int dni = Integer.parseInt(tDni.getText());
            String domicilio = tDomicilio.getText();
            String celular = tCelular.getText();
            Cliente cliente = new Cliente(id, nombre, dni, domicilio, celular);
            clienteData.actualizarCliente(cliente);
        }
    }//GEN-LAST:event_jbActualizarActionPerformed

    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        int id=Integer.parseInt(tId.getText());
        Cliente cliente = clienteData.buscarCliente(id);
        if (cliente != null){
            tNombre.setText(cliente.getNombreC());
            int dni = cliente.getDni();
            tDni.setText(dni+"");
            tDomicilio.setText(cliente.getDomicilio());
            tCelular.setText(cliente.getCelular());
        } else { tNombre.setText("");
                 tDni.setText("");
                 tDomicilio.setText("");
                 tCelular.setText("");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed


