    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        String nroSerie = tNroSerie.getText();
        String tipo = tTipo.getText();
        Cliente dueño = new Cliente();
        dueño.setIdCliente(Integer.parseInt(tDueño.getText()));
        Date fechaIngreso = Date.valueOf(tFechaIngreso.getText());
        Date fechaEgreso = Date.valueOf(tFechaEgreso.getText());        
        Aparato aparato = new Aparato(nroSerie, tipo, dueño, fechaIngreso, fechaEgreso);
        aparatoData.guardarAparato(aparato);
        tId.setText(aparato.getIdAparato()+"");
    }//GEN-LAST:event_jbGuardarActionPerformed

