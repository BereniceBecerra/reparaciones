    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        int id=Integer.parseInt(tId.getText());
        Aparato aparato = aparatoData.buscarAparato(id);
        if (aparato != null){
            tNroSerie.setText(aparato.getNroSerie());
            tTipo.setText(aparato.getTipo());
            tDueño.setText(aparato.getDueño().getIdCliente()+"");
            tFechaIngreso.setText(aparato.getFechaIngreso()+"");
            tFechaEgreso.setText(aparato.getFechaEgreso()+"");
        } else { tNroSerie.setText("");
                 tTipo.setText("");
                 tDueño.setText("");
                 tFechaIngreso.setText("");
                 tFechaEgreso.setText("");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarActionPerformed
        if (tId.getText() != null){
            int id=Integer.parseInt(tId.getText());
            String nroSerie = tNroSerie.getText();
            String tipo = tTipo.getText();
            Cliente dueño = new Cliente();
            dueño.setIdCliente(Integer.parseInt(tDueño.getText()));
            Date fechaIngreso = Date.valueOf(tFechaIngreso.getText());
            Date fechaEgreso = Date.valueOf(tFechaEgreso.getText());        
            Aparato aparato = new Aparato(id, nroSerie, tipo, dueño, fechaIngreso, fechaEgreso);
            aparatoData.actualizarAparato(aparato);
        }
    }//GEN-LAST:event_jbActualizarActionPerformed


