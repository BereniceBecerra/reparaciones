    private void jbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarActionPerformed
        if (tId.getText() != null){
            int id=Integer.parseInt(tId.getText());
            int codigo=Integer.parseInt(tCodigo.getText());
            String descripcion = tDescripcion.getText();
            Double costo = Double.parseDouble(tCosto.getText());
            Servicio servicio = new Servicio(id, codigo, descripcion, costo);
            servicioData.actualizarServicio(servicio);
        }
    }//GEN-LAST:event_jbActualizarActionPerformed

    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        int id=Integer.parseInt(tId.getText());
        Servicio servicio = servicioData.buscarServicio(id);
        if (servicio != null){
            tCodigo.setText(servicio.getCodigo()+"");
            tDescripcion.setText(servicio.getDescripcion());
            tCosto.setText(servicio.getCosto()+"");
        } else { tCodigo.setText("");
                 tDescripcion.setText("");
                 tCosto.setText("");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed


