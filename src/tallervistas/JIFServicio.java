    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        int codigo=Integer.parseInt(tCodigo.getText());
        String descripcion = tDescripcion.getText();
        Double costo = Double.parseDouble(tCosto.getText());
        Servicio servicio = new Servicio(codigo, descripcion, costo);
        servicioData.guardarServicio(servicio);
        tId.setText(servicio.getIdServicio()+"");
    }//GEN-LAST:event_jbGuardarActionPerformed

    private void jbBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBorrarActionPerformed
        int id=Integer.parseInt(tId.getText());
        servicioData.borrarServicio(id);
    }//GEN-LAST:event_jbBorrarActionPerformed

